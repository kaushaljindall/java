package chapter2;

public class datatypes {
    public static void main(String[] args) {
        // Intergers

        byte b = 5;
        short c = 10; 
        int i = 2000;
        long l = 12494;
        


        // Real Numbers

        float pi = 3.1415f;
        double pii = 3.1415;


        System.out.println("Integer Values: " + b + ", "  + c +", " + i + ", " + l);
        System.out.println("Real Values: " + pi + ", "  + pii);

    }
}
